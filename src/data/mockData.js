// Helper function to get people grouped by department and job title
export function getPeopleByDepartment() {
  return mockPeople.reduce((acc, person) => {
    if (!acc[person.department]) {
      acc[person.department] = {};
    }
    if (!acc[person.department][person.jobTitle]) {
      acc[person.department][person.jobTitle] = [];
    }
    acc[person.department][person.jobTitle].push(person);
    return acc;
  }, {});
}

// Extract unique departments from the data
export const getDepartments = () => {
  const departmentSet = new Set();
  mockPeople.forEach(person => departmentSet.add(person.department));
  return Array.from(departmentSet).sort();
};

// Get unique departments with counts
export const getDepartmentsWithCounts = () => {
  const departmentCounts = new Map();
  mockPeople.forEach(person => {
    departmentCounts.set(person.department, (departmentCounts.get(person.department) || 0) + 1);
  });
  return Array.from(departmentCounts.entries())
    .map(([name, count]) => ({ name, count }))
    .sort((a, b) => a.name.localeCompare(b.name));
};

